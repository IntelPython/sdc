"""
Expect Series
0         NaN
1         NaN
2   -0.340000
3         NaN
4    1.787879
dtype: float64
"""
import numpy as np
import pandas as pd
from numba import njit


@njit
def series_pct_change():
    s = pd.Series([5., 0, 3.3, np.nan, 9.2])

    return s.pct_change(periods=2, fill_method=None, limit=None, freq=None)


print(series_pct_change())
