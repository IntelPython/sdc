# Expected:
# London      2.995732
# New York    3.044522
# Helsinki    2.484907
# dtype: float64

import pandas as pd
import numpy as np
from numba import njit


@njit
def series_apply():
    s = pd.Series([20, 21, 12],
                  index=['London', 'New York', 'Helsinki'])

    return s.apply(np.log)


print(series_apply())
